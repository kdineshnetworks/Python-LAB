#Logical operators are used to combine multiple conditions and return a boolean result (True or False). Here are some common logical operators in Python:
# and (returns True if both conditions are True)
# or (returns True if at least one condition is True)
# not (returns True if the condition is False)
a=4
b=5
c=6
if(a<b and b<c):
    print("true")
else:
    print("false")  

if not(a<b):
    print("true")   
else:   
    print("false")
